const quantity = document.getElementById("quantity");
const convertFrom = document.getElementById("convert_from");
const convertTo = document.getElementById("convert_to");

function replaceText(selector, text) {
    const element = document.getElementById(selector)
    if (element) element.innerText = text
}

quantity.onchange    = update;
convertFrom.onchange = update;
convertTo.onchange   = update;

function update() {
    console.log("Quantity: " + quantity.value 
        + "\nconvertFrom: " + convertFrom.value 
        + "\nconvertTo: " + convertTo.value
        + "\n" + conversions[convertFrom.value + " " + convertTo.value])

        var factor = conversions[convertFrom.value + " " + convertTo.value];
        var result = quantity.value * factor;
        if (result < 0) {
            replaceText("result", "You must be making void muffins.");
        } else if (result > 10000) {
            replaceText("result", "Wow. That's a lot of flour.");
        } else
            replaceText("result", result.toFixed(2))

    window.electron.send("values-changed", {someData: "Hello"})
}

